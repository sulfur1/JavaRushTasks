package com.javarush.task.task20.task2027;

public class Point {
    private int X;
    private int Y;

    Point() {}

    public int getX() {
        return X;
    }

    public int getY() {
        return Y;
    }

    public void setX(int x) {
        X = x;
    }

    public void setY(int y) {
        Y = y;
    }
}
